// vector<vector<int>> v(3, vector<int>(4,0) 3x4 filled with 0s

void solve() {
  string s;
  unordered_map<string,int> map;
  int tot=0;
  while (getline(cin,s)) {
    if (map.find(s)==map.end()) map[s]=0;
    map[s]++;
    tot++;
  }
  vector<pair<string,double>> v;
  for (auto &it:map) {
    v.push_back({it.first,(double)it.second/(double)tot*100.0});
  }
  sort(v.begin(),v.end());
  for (auto p:v) {
    cout<<p.first<<" ";
    cout<<fixed<<setprecision(4)<<p.second<<endl;
  }
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}