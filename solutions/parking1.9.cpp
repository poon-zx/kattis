void solve() {
  vector<int> v(101);
  int a,b,c;
  cin>>a>>b>>c;
  for (int i=0;i<3;i++) {
    int f,s;
    cin>>f>>s;
    for (int j=f;j<s;j++) {
      v[j]++;
    }
  }
  int res=0;
  for (auto num:v) {
    if (num==1) {res+=a;}
    if (num==2) {res+=2*b;}
    if (num==3) {res+=3*c;}
  }
  cout<<res;
}

