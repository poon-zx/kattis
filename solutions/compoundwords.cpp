void solve() {
  set<string> se;
  string s;
  vector<string> v;
  while(cin>>s) {
    v.push_back(s);
  }
  for(int i=0;i<v.size();i++) {
    for(int j=i+1;j<v.size();j++) {
      se.insert(v[i]+v[j]);
      se.insert(v[j]+v[i]);
    }
  }
  for(auto &word:se) cout<<word<<endl;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}