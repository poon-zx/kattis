void solve() {
  int n;
  cin>>n;
  double res=0;
  for (int i=0;i<n;i++) {
    double a,b,c;
    cin>>a>>b>>c;
    double angle=acos((a*a+b*b-c*c)/(2.0*a*b));
    double ratio=(a+b*cos(angle))/(b*sin(angle));
    double r=a/sqrt(1.0+ratio*ratio);
    res+=2*r;
  }
  cout<<fixed<<setprecision(4)<<res;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}