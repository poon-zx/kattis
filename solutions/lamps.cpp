void solve() {
    int h, P;
    cin >> h >> P;
    int D = 1; 
    while (true) {
        int H = D * h;
        int bulbs = (H + 999) / 1000;
        long long cost_inc = 5LL * bulbs * 100000 + 60LL * H * P;
        long long cost_low = 60LL * 100000 + 11LL * H * P;
        
        if (cost_low < cost_inc) {
            cout << D;
            return;
        }
        D++;
    }
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}