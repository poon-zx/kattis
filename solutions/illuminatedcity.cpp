// vector<vector<int>> v(3, vector<int>(4,0) 3x4 filled with 0s

void solve() {
  int n;
  i64 x;
  double y;
  cin>>n>>x>>y;
  vector<ld> v;
  for (int i=0;i<n;i++) {
    ld t;
    cin>>t;
    v.push_back(t);
  }
  sort(v.begin(),v.end());
  ld total=0;
  int counter=1;
  for (int i=0;i<n;i++) {
    total+=v[i]*x;
    float avg=total/counter;
    if (avg>y) {
      break;
    }
    counter++;
  }
  cout<<counter-1;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}