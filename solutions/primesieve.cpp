vector<bool> sieve(int n){
    vector<bool> is_prime(n+1, true);
    is_prime[0] = is_prime[1] = false;
    for (int i = 2; i <= n; i++) {
        if (is_prime[i] && (long long)i * i <= n) {
            for (int j = i * i; j <= n; j += i)
                is_prime[j] = false;
        }
    }
    return is_prime;
}

void solve() {
  int n,q;
  cin>>n>>q;
  vector<bool> primes=sieve(n);
  int res=0;
  for(int i=0;i<primes.size();i++) {
    if(primes[i]) res++;
  }
  cout<<res<<"\n";
  while(q--) {
    int x;
    cin>>x;
    cout<<(primes[x]?1:0)<<"\n";
  }
}

