void solve() {
  int g,r;
  cin>>g>>r;
  int res=0;
  while (r>0&&g>0) {
    r--;
    g--;
    res+=10;
  }
  while (g>2) {
    g-=3;
    res+=10;
  }
  if (g==2) {
    res+=3;
  } else if (g==1) {
    res++;
  }
  cout<<res;
}

