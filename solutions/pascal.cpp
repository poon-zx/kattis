bool isPrime(int number){

    if(number < 2) return false;
    if(number == 2) return true;
    if(number % 2 == 0) return false;
    for(int i=3; (i*i)<=number; i+=2){
        if(number % i == 0 ) return false;
    }
    return true;

}

void solve() {
  ll n;
  cin>>n;
  if(n==1) {cout<<0;return;}
  int small=n;
  for(int i=2;i*i<=n;i++) {
    if(n%i==0&&isPrime(i)) {
      small=i;
      break;
    }
  }
  cout<<n-(n/small);

}

