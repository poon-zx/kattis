void solve() {
  string s;
  cin>>s;
  int n=s.size();
  int res=n;
  for (int len=1;len<=n;len++) {
    for (int i=0;i<n;i++) {
      string macro=s.substr(i,len);
      int tot=0;
      int j=0;
      while (j<n) {
        if (s.substr(j,len)==macro) {
          tot++;
          j+=len;
        } else j++;
      }
      res=min(res,n-tot*(len-1)+len);
    }
  }
  cout<<res;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}