// vector<vector<int>> v(3, vector<int>(4,0) 3x4 filled with 0s

void solve() {
  ll n;
  cin>>n;
  ll l=2,power=1;
  ll cum=0;
  while (cum+pow(l,power)<n) {
    cum+=pow(l,power);
    power++;
  }
  ll offset=n-1-cum;
  vector<int> v;
  for (ll i=0;i<power;i++) {
    if (offset&(1LL<<(power-1-i))) v.push_back(7);
    else v.push_back(4);
  }
  for (auto num:v) cout<<num;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}