void solve() {
  int n,m;
  cin>>n>>m;
  vector<pair<int,int>> res;
  for (int i=1;i<=n;i++) {
    for (int j=1;j<=m;j++) {
      char c;
      cin>>c;
      if(c=='*') res.push_back({i,j});
    }
  }
  cout<<res.size()<<"\n";
  for (auto &[x,y]:res) cout<<x<<" "<<y<<"\n";
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}