void solve() {
  int r,b;
  cin>>r>>b;
  int total=r+b;
  int l=0,w=0;
  for (int i=3;i<total;i++) {
    if (total%i==0) {
      int th=i;
      int tw=total/i;
      if ((th-2)*(tw-2)==b) {
        l=max(th,tw);
        w=min(th,tw);
      }
    }
  }
  cout<<l<<" "<<w;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}