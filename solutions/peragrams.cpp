void solve() {
  string s;
  cin>>s;
  vector<int> v(26);
  for (char c:s) {
    v[c-'a']++;
  }
  bool odd=false;
  int res=0;
  for (auto& i:v) {
    if (i==0) continue;
    if (i%2==1&&!odd) {
      res+=i;
      odd=true;
    } else if (i%2==0) res+=i;
    else res+=(i-1);
  }
  cout<<s.size()-res;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}